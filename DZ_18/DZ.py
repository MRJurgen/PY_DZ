quantity=int(input('Количество элементов в массиве : '))
value_to_find=int(input('Какое число искать : '))

value = 0
my_list = []

for i in range(quantity):
    my_list.insert(i,i+1)
    

for i in range(quantity):
    
    if (my_list[i]<=value_to_find):
        value=my_list[i]
    else:
        value=my_list[i]
        break
    
print(my_list)
print("Ближайшее : ",value)